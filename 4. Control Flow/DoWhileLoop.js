// The do/while loop is a variant of the while loop. This loop will execute the code block once, before checking if the condition is true, then it will repeat the loop as long as the condition is true.
let i=0;
do{
    console.log(i);
    i++;
}while(i<=10)

let i=1;
do{
    console.log(i*2);
    i++;
}while(i<=10)